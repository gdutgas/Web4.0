            <div class="headline">
                <img src="img/tubiao1.png" class="hl1" >
                <div class="hl2"><h1 class="title"  id="6" >课程发展历史沿革</h1></div>
                <a href="#box"><img src="img/tubiao2.png" width="600" height="40"  class="hl3"></a>

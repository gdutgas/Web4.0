        <div>                                                              <!--教学的设计思想-->
            <div class="headline">
                <img src="img/tubiao1.png" class="hl1" >
                <div class="hl2"><h1 class="title"  id="7" >教学的设计思想</h1></div>
                <a href="#box"> <img src="img/tubiao2.png" width="630" height="40"  class="hl3"></a>
            </div>
            <h3>（一）概念讲述注重物理意义的分析</h3>
            <p>本课程区别于其它专业基础课程的一大特色：信息理论部分抽象概念较多，如信息熵、平均互信息等。一般来说这些概念都是由数学模型引入，进而分析数学表达式，数学性质等，难于理解和记忆。而这些抽象概念的理解是课程学习的基础，因此，课程讲述时必须注重抽象概念物理意义的分析，让学生从具体的物理现象理解抽象概念，加强学生对概念的拓展和应用。</p>
            <h3>（二）将先进的通信技术引入课堂</h3>
            <p>通信技术发展日新月异，学生对先进的通信技术始终充满着好奇心，具有极强的探索欲望。在信息理论对于通信工程的指导意义中，引入了先进通信技术,一方面用于说明理论对于实践的指导，另一方面拓宽了学生的知识面，有利于加深其对信息理论的理解，提高了学生学习热情。</p>
            <h3>（三）采取多样化的教学方法</h3>
            <p>在《信息论与编码》课程的教学过程中，采用反转教学方法，避免“满堂灌”的情况，尽可能多地调动学生主动参与课堂的热情。我们将课程中部分内容留给某些学生，让他们自由发挥，进行讲授，扮演“教师”角色；将课程中某些重要内容（以问题形式给出）作为研讨议题，学生组织，教师引导，自由讨论。这些措施较好地调动学生的学习积极性和主动性，激发学生的求知欲与好奇心。讨论中不把相关结论直接告诉学生，而是要求学生在分析比较的基础上，自己总结归纳。这样学生既学会了思考，又在发现问题、分析问题、解决问题的过程中获得了知识，进而培养了他们自学的能力，有利于形成创新性思维。</p>
            <h3>（四）实践教学与理论教学相辅相成</h3>
            <p>《信息论与编码》课程的内容较为抽象，但其结论对通信技术有直接的指导作用。因此，通过合理设计实践教学内容和方式，可以有效促进理论教学。我们设计了多层次的课程实验，并且结合参考、实习，让学生对抽象的理论知识有更具体感性的认识。等通信体制的关键技术。实践教学与理论教学相互融合，相辅相成，将部分讲授内容以实践教学形式完成，而实践教学的一些结论又可作为课程教学有效的补充。实现理论与实践的有机结合，通过实践学习理论，利用理论来指导实践。</p>
            <h3>（五）多种实践教学手段提高学生综合能力</h3>
            <p>在《信息论与编码》的实践教学中，以课程实验为重点建设内容，严把“课题设计关”、“组织指导关”及“考核验收关”。针对学生所选择的实验科目，我们将相应实验的目的、内容及要求以“实验任务书”的形式发放给学生，要求学生自己拟定方案和步骤，对学生的能力培养起到了积极作用。在考核评估方面，要求学生必须按时完成实验，教师对实验结果进行现场检查验收，并要求准时递交实验报告，以克服学生杜撰、抄袭实验结果的弊端。教师根据实验表现和实验报告的具体内容综合评定其成绩。对于创新型实验，还需进行开题论证和结题报告等考核。学生通过查阅资料、开题论证、分组讨论、结题答辩等方式不仅扩展了所学的知识，而且体验了科学研究的全过程，激发了其学习和钻研的热情。这些方法使学生分析问题和解决问题的能力得到了极大地锻炼和加强，同时也培养了学生的团队协作精神。通过各种课程实验及课程设计的锻炼，提升了学生使用编程语言工具的能力，为日后的学习和实际工作打下了基础。</p>

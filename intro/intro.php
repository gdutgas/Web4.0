            <p>本课程作为电子科学与工程学院的一门重要的学科基础课程，经过多年的发展与建设，无论在师资队伍建设、教学改革、教材建设方面，还是在网络教学、实践教学、课件制作等各个方面均取得了一定的成绩。课程组历届负责人都以其扎实的学术修养和兢兢业业教书育人的师德师风受到广大师生的尊敬，在国内具有一定影响。同时课程组拥有一支稳定而充满活力的教学队伍，其学历层次和年龄结构合理。这些老师长期从事信息论与编码领域的科学研究，承担了数十项国家、军队及企业委托的研发项目，他们中有的是高产作者，有的是国优、省优论文获得者，在国内同行中产生了一定的学术知名度。他们经过层层筛选进入课程组，在老教员热情的传帮带下，在课程组持之以恒的严格制度督导下，迅速成长为优秀教师，其带队参赛和指导的学生在全国大学生电子设计及其信息安全专题邀请赛上频频获奖。

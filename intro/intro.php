            <h3>（一）信息论在信息科学中的重要地位</h3>
            <p>信息论、控制论和系统论并称为信息科学的三大支柱理论，日益受到各方面的重视。信息理论不仅在通信领域发挥了越来越重要的作用，同时也迅速渗透到其他相关的自然科学与社会科学领域，对信息科学和信息产业有重要促进作用。</p>
            <h3>（二）该课程作为研究生学位课的重要地位</h3>
            <p>随着通信技术的不断发展，《信息论与编码》作为研究生学位课已经变得越来越重要，对相关课程及专业的普及与提高具有积极推动作用。多年的实践证明，该门课程为学生将来从事信息与通信领域研究开发和工程应用打下坚实的理论基础。可以帮助学生站在更高的角度理解当今数字化、信息化技术的发展与应用，对学生今后的发展具有重要意义。目前许多其他高校的信息类专业也都逐渐将《信息论与编码》作为研究生学位必修课程。</p>
            <h3>(三）课程发展过程</h3>
            <p>我院最初使用的教材《信息论基础理论与应用》是面向21世纪电子信息类教材，傅祖芸主编，电子工业出版社发行。该教材是在总结和借鉴国内外信息论教材的基础上编写而成，比较适合我校通信和信息工程专业。2005年之前，该课程只是讲授信息论部分，课程名称是《信息论基础》；从2005年申报者来我校工作开始加入编码理论，该课程的名称改为《信息论与编码》。</p>
            <h3>（四）教学内容发展变化和教学改革</h3>
            <p>由于信息论方面的许多教材内容十分抽象、难以理解。在长期教学实践中，我们对该课程进行了一些改革和探索，取得了一些成果，即针对工科研究生特点，在注重理论研究基础上，结合实际应用背景，补充阐述概念/定理的物理意义和应用环境。妥善处理好深度与广度的关系，加强理论与应用的结合，注重课程的实践性教学，让学生体验到将抽象理论应用于工程技术的基本原理和方法，深刻体会理论的严密性和工程应用的实用性。</p>
            <h3>（五）总体建设目标</h3>
            <p>我们的指导思路是：“以数学原理为基础，立足工程应用”，在教学内容、教学方法和手段、教学管理等方面进行改革，在授课质量和实践教学上严格把关，取得了良好的效果，利用先进的教学理念和扎实有效的工作，使本课程的整体建设水平与省内同类大学精品课程相当。</p>
